package basic;

public class Variable {
    public static void main(String[] args) {
        int i = 1;
        float f = 0.1f;
        long l = 10l;
        double d = 0.1;
        String s = "string";

        System.out.println(i);
        System.out.println(f);
        System.out.println(l);
        System.out.println(d);
        System.out.println(s);
    }
}
